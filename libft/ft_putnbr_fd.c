#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int	nb;

	nb = 0;
	if (n == -2147483648)
	{
		write (fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n >= 0 && n <= 9)
	{
		nb = n + 48;
		write (fd, &nb, 1);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
